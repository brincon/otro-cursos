# pedirle al usuario el ancho y el alto de un rectaangulo y decirle si es un cuadrado

alto = int(input("Por favor ingrese el alto: "))
ancho = int(input("Por favor ingrese el ancho: "))

if alto == ancho:
    print ("El rectángulo es un cuadrado")
else:
    print("El rectángulo no es un cuadrado")

